import { useState, useEffect } from 'react'
import dofusDataImporter from '../services/dofusDataImporter.js'
import autoImportService from '../services/autoImportService.js'
import debugDofusAPI from '../utils/debugDofusAPI.js'

const DataImporter = ({ isOpen, onClose }) => {
  const [importing, setImporting] = useState(false)
  const [updating, setUpdating] = useState(false)
  const [stats, setStats] = useState(null)
  const [importLog, setImportLog] = useState([])
  const [progress, setProgress] = useState(0)
  const [autoStatus, setAutoStatus] = useState(null)
  const [availableJobs, setAvailableJobs] = useState([])
  const [jobImporting, setJobImporting] = useState({})
  const [selectedJobItems, setSelectedJobItems] = useState({})
  const [loadingItems, setLoadingItems] = useState({})

  // Charger les statistiques au d√©marrage
  useEffect(() => {
    if (isOpen) {
      loadStats()
      loadAutoStatus()
      loadAvailableJobs()
    }
  }, [isOpen])

  const loadStats = async () => {
    try {
      console.log('üîÑ Chargement des statistiques...')
      const importStats = await dofusDataImporter.getImportStats()
      console.log('üìä Stats re√ßues:', importStats)
      setStats(importStats)
    } catch (error) {
      console.error('‚ùå Erreur chargement stats:', error)
      setStats({
        totalItems: 0,
        byProfession: {},
        lastUpdate: null
      })
    }
  }

  const loadAutoStatus = () => {
    const status = autoImportService.getStatus()
    setAutoStatus(status)
  }

  const loadAvailableJobs = async () => {
    try {
      const jobs = await dofusDataImporter.fetchAllJobs()
      console.log('üîß M√©tiers charg√©s:', jobs)

      // V√©rifier que jobs est un array et que chaque job a les bonnes propri√©t√©s
      const validJobs = Array.isArray(jobs) ? jobs.filter(job => {
        const isValid = job &&
          typeof job === 'object' &&
          (job.id !== undefined && job.id !== null) &&
          (job.name !== undefined && job.name !== null)

        if (!isValid) {
          console.warn('‚ö†Ô∏è M√©tier invalide:', job)
        }

        return isValid
      }).map(job => {
        // Normaliser le nom du m√©tier (extraire le fran√ßais si c'est un objet multilingue)
        let jobName = job.name
        if (typeof jobName === 'object' && jobName !== null) {
          jobName = jobName.fr || jobName.en || jobName.de || jobName.es || jobName.pt || `M√©tier ${job.id}`
        }

        return {
          ...job,
          name: jobName
        }
      }) : []

      setAvailableJobs(validJobs)
      console.log(`‚úÖ ${validJobs.length} m√©tiers valides charg√©s`)
    } catch (error) {
      console.error('Erreur chargement m√©tiers:', error)
      setAvailableJobs([])
    }
  }

  const addLog = (message, type = 'info') => {
    const timestamp = new Date().toLocaleTimeString()
    setImportLog(prev => [...prev, { timestamp, message, type }])
  }

  // Test de connexion √† la base de donn√©es
  const testDatabaseConnection = async () => {
    try {
      addLog('üîç Test de connexion √† la base de donn√©es...', 'info')

      const dbUrl = import.meta.env.DEV
        ? 'http://localhost:8888/.netlify/functions/database'
        : '/.netlify/functions/database'

      addLog(`üì° URL test√©e: ${dbUrl}`, 'info')

      const response = await fetch(`${dbUrl}?action=get_import_stats`)
      addLog(`üì° R√©ponse: ${response.status} ${response.statusText}`, response.ok ? 'success' : 'error')

      if (response.ok) {
        const data = await response.json()
        addLog(`üìä Donn√©es re√ßues: ${JSON.stringify(data).substring(0, 200)}...`, 'info')
        addLog(`üìä Type: ${typeof data}, Array: ${Array.isArray(data)}, Longueur: ${Array.isArray(data) ? data.length : 'N/A'}`, 'info')
      } else {
        const errorText = await response.text()
        addLog(`‚ùå Erreur: ${errorText}`, 'error')
      }
    } catch (error) {
      addLog(`‚ùå Erreur de connexion: ${error.message}`, 'error')
    }
  }

  const handleFullImport = async () => {
    if (importing) return

    setImporting(true)
    setImportLog([])
    setProgress(0)

    try {
      addLog('üîß Import forc√© par admin...', 'info')

      const result = await autoImportService.forceFullImport()

      if (result.success) {
        addLog(`‚úÖ Importation termin√©e ! ${result.totalItems} objets import√©s depuis ${result.totalJobs} m√©tiers`, 'success')
        setProgress(100)
        await loadStats()
        loadAutoStatus()
      } else {
        addLog('‚ùå Erreur lors de l\'importation', 'error')
      }
    } catch (error) {
      addLog(`‚ùå Erreur: ${error.message}`, 'error')
    } finally {
      setImporting(false)
    }
  }

  const handleUpdate = async () => {
    if (updating) return

    setUpdating(true)
    setImportLog([])

    try {
      addLog('üîß Mise √† jour forc√©e par admin...', 'info')

      const result = await autoImportService.forceUpdate()

      if (result.success) {
        addLog(`‚úÖ Mise √† jour termin√©e ! ${result.newItems} nouveaux objets ajout√©s`, 'success')
        await loadStats()
        loadAutoStatus()
      } else {
        addLog('‚ùå Erreur lors de la mise √† jour', 'error')
      }
    } catch (error) {
      addLog(`‚ùå Erreur: ${error.message}`, 'error')
    } finally {
      setUpdating(false)
    }
  }

  const handleDebugAPI = async () => {
    setImportLog([])
    addLog('üîç D√©but du debug API DofusDB...', 'info')

    try {
      // Lancer le debug complet
      await debugDofusAPI.runFullDebug()
      addLog('‚úÖ Debug termin√© - V√©rifiez la console pour les d√©tails', 'success')
    } catch (error) {
      addLog(`‚ùå Erreur debug: ${error.message}`, 'error')
    }
  }

  const handleJobImport = async (jobId, jobName) => {
    if (jobImporting[jobId]) return

    setJobImporting(prev => ({ ...prev, [jobId]: true }))
    addLog(`üéØ Import du m√©tier: ${jobName}`, 'info')

    try {
      const result = await dofusDataImporter.importSingleJob(jobId, jobName)

      if (result.success) {
        const icon = getJobIcon(jobName)
        if (result.totalItems > 0) {
          addLog(`${icon} ${jobName}: ${result.totalItems} objets import√©s depuis ${result.totalRecipes} recettes`, 'success')
          if (result.skippedItems > 0) {
            addLog(`‚ö†Ô∏è ${jobName}: ${result.skippedItems} recettes ignor√©es (sans r√©sultat valide)`, 'warning')
          }
        } else {
          addLog(`${icon} ${jobName}: ${result.message || 'Aucun objet √† importer'}`, 'info')
        }
        await loadStats()
        loadAutoStatus()

        // Recharger les items du m√©tier si ils sont affich√©s
        if (selectedJobItems[jobName]) {
          await loadJobItems(jobName)
        }
      } else {
        addLog(`‚ùå Erreur ${jobName}: ${result.error}`, 'error')
      }
    } catch (error) {
      addLog(`‚ùå Erreur import ${jobName}: ${error.message}`, 'error')
    } finally {
      setJobImporting(prev => ({ ...prev, [jobId]: false }))
    }
  }

  const loadJobItems = async (jobName) => {
    if (loadingItems[jobName]) return

    setLoadingItems(prev => ({ ...prev, [jobName]: true }))

    try {
      const baseUrl = import.meta.env.DEV
        ? 'http://localhost:8888/.netlify/functions/database'
        : '/.netlify/functions/database'

      const response = await fetch(`${baseUrl}?action=get_items_by_profession&profession=${encodeURIComponent(jobName)}`)

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`)
      }

      const items = await response.json()
      setSelectedJobItems(prev => ({ ...prev, [jobName]: items }))
      addLog(`üìã ${items.length} objets charg√©s pour ${jobName}`, 'info')
    } catch (error) {
      console.error(`Erreur chargement items ${jobName}:`, error)
      addLog(`‚ùå Erreur chargement items ${jobName}: ${error.message}`, 'error')
    } finally {
      setLoadingItems(prev => ({ ...prev, [jobName]: false }))
    }
  }

  const cleanJobDuplicates = async (jobName) => {
    try {
      const baseUrl = import.meta.env.DEV
        ? 'http://localhost:8888/.netlify/functions/database'
        : '/.netlify/functions/database'

      const response = await fetch(`${baseUrl}?action=clean_duplicates`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ profession: jobName })
      })

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`)
      }

      const result = await response.json()
      addLog(`üßπ ${result.deletedCount} doublons supprim√©s pour ${jobName}`, 'success')

      // Recharger les stats et items
      await loadStats()
      if (selectedJobItems[jobName]) {
        await loadJobItems(jobName)
      }
    } catch (error) {
      console.error(`Erreur nettoyage ${jobName}:`, error)
      addLog(`‚ùå Erreur nettoyage ${jobName}: ${error.message}`, 'error')
    }
  }

  const extractResources = async () => {
    try {
      addLog('üîç Extraction des ressources depuis les recettes...', 'info')

      const baseUrl = import.meta.env.DEV
        ? 'http://localhost:8888/.netlify/functions/database'
        : '/.netlify/functions/database'

      const response = await fetch(`${baseUrl}?action=extract_resources`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        }
      })

      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`)
      }

      const result = await response.json()

      if (result.success) {
        addLog(`‚úÖ ${result.resourcesCount} ressources extraites et sauvegard√©es`, 'success')
        addLog(`‚úÖ ${result.linksCount} liaisons item-ressource cr√©√©es`, 'success')
      } else {
        addLog(`‚ùå Erreur extraction: ${result.error}`, 'error')
      }
    } catch (error) {
      console.error('Erreur extraction ressources:', error)
      addLog(`‚ùå Erreur extraction ressources: ${error.message}`, 'error')
    }
  }

  const formatDate = (timestamp) => {
    if (!timestamp) return 'Jamais'
    return new Date(timestamp).toLocaleString()
  }

  // Obtenir l'ic√¥ne d'un m√©tier
  const getJobIcon = (jobName) => {
    const icons = {
      'Alchimiste': 'üß™',
      'Forgeron d\'√âp√©es': '‚öîÔ∏è',
      'Forgeron de Dagues': 'üó°Ô∏è',
      'Forgeron de Marteaux': 'üî®',
      'Forgeron de Pelles': '‚õèÔ∏è',
      'Forgeron de Haches': 'ü™ì',
      'Sculpteur d\'Arcs': 'üèπ',
      'Sculpteur de B√¢tons': 'ü™Ñ',
      'Sculpteur de Baguettes': '‚ú®',
      'Cordonnier': 'üë¢',
      'Joaillomage': 'üíé',
      'Tailleur': 'üßµ',
      'Forgeron de Boucliers': 'üõ°Ô∏è',
      'Bricoleur': 'üîß',
      'Mineur': '‚õèÔ∏è',
      'B√ªcheron': 'ü™ì',
      'P√™cheur': 'üé£',
      'Chasseur': 'üèπ',
      'Paysan': 'üåæ',
      'Boucher': 'ü•©',
      'Poissonnier': 'üêü',
      'Boulanger': 'üçû'
    }

    return icons[jobName] || '‚öíÔ∏è' // Ic√¥ne par d√©faut
  }

  if (!isOpen) return null

  return (
    <div className="importer-modal-overlay" onClick={(e) => e.target === e.currentTarget && onClose()}>
      <div className="importer-modal">
        <div className="importer-header">
          <h2>üîß Importation des Donn√©es DofusDB</h2>
          <button onClick={onClose} className="modal-close-btn">‚úï</button>
        </div>

        <div className="importer-content">
          {/* Statut Auto-Import */}
          <div className="auto-import-status">
            <h3>ü§ñ Statut Auto-Importation</h3>
            {autoStatus ? (
              <div className="status-grid">
                <div className="status-item">
                  <div className="status-label">Statut</div>
                  <div className={`status-value ${autoStatus.importInProgress ? 'importing' : 'idle'}`}>
                    {autoStatus.importInProgress ? 'üîÑ Import en cours...' : '‚úÖ Actif'}
                  </div>
                </div>
                <div className="status-item">
                  <div className="status-label">Derni√®re v√©rification</div>
                  <div className="status-value">{autoStatus.lastCheck}</div>
                </div>
                <div className="status-item">
                  <div className="status-label">Dernier import</div>
                  <div className="status-value">{autoStatus.lastImport}</div>
                </div>
                <div className="status-item">
                  <div className="status-label">Prochaine v√©rification</div>
                  <div className="status-value">{autoStatus.nextCheck}</div>
                </div>
                <div className="status-item">
                  <div className="status-label">Temps depuis dernier import</div>
                  <div className="status-value">{autoStatus.timeSinceLastImport}</div>
                </div>
                <div className="status-item">
                  <div className="status-label">Mise √† jour n√©cessaire</div>
                  <div className={`status-value ${autoStatus.needsUpdate ? 'needs-update' : 'up-to-date'}`}>
                    {autoStatus.needsUpdate ? '‚ö†Ô∏è Oui' : '‚úÖ Non'}
                  </div>
                </div>
              </div>
            ) : (
              <div className="loading">Chargement du statut...</div>
            )}
          </div>

          {/* Statistiques */}
          <div className="importer-stats">
            <h3>üìä Statistiques actuelles</h3>
            {stats ? (
              <div className="stats-grid">
                <div className="stat-item">
                  <div className="stat-value">{stats.totalItems}</div>
                  <div className="stat-label">Objets craftables</div>
                </div>
                <div className="stat-item">
                  <div className="stat-value">{Object.keys(stats.byProfession).length}</div>
                  <div className="stat-label">M√©tiers</div>
                </div>
                <div className="stat-item">
                  <div className="stat-value">{formatDate(stats.lastUpdate)}</div>
                  <div className="stat-label">Derni√®re mise √† jour</div>
                </div>
              </div>
            ) : (
              <div className="loading">Chargement des statistiques...</div>
            )}

            {stats && stats.byProfession && (
              <div className="profession-stats">
                <h4>R√©partition par m√©tier :</h4>
                <div className="profession-list">
                  {Object.entries(stats.byProfession).map(([profession, count]) => (
                    <div key={profession} className="profession-item">
                      <span className="profession-name">{profession}</span>
                      <span className="profession-count">{count} objets</span>
                    </div>
                  ))}
                </div>
              </div>
            )}
          </div>

          {/* Actions */}
          <div className="importer-actions">
            <h3>‚öôÔ∏è Actions</h3>
            <div className="action-buttons">
              <button
                onClick={testDatabaseConnection}
                className="btn btn-secondary"
              >
                üîç Test BDD
              </button>
              <button
                onClick={handleFullImport}
                disabled={importing || updating}
                className="btn btn-primary btn-import"
              >
                {importing ? 'üîÑ Importation en cours...' : 'üì• Importation compl√®te'}
              </button>

              <button
                onClick={handleUpdate}
                disabled={importing || updating}
                className="btn btn-secondary btn-update"
              >
                {updating ? 'üîÑ Mise √† jour...' : 'üîÑ Mise √† jour incr√©mentale'}
              </button>

              <button
                onClick={handleDebugAPI}
                disabled={importing || updating}
                className="btn btn-warning btn-debug"
              >
                üîç Debug API
              </button>

              <button
                onClick={extractResources}
                disabled={importing || updating}
                className="btn btn-info"
                title="Extraire les ressources depuis les recettes"
              >
                üß™ Extraire Ressources
              </button>
            </div>

            <div className="action-descriptions">
              <div className="action-desc">
                <strong>Importation compl√®te :</strong> R√©cup√®re tous les m√©tiers et toutes leurs recettes depuis DofusDB. Peut prendre plusieurs minutes.
              </div>
              <div className="action-desc">
                <strong>Mise √† jour incr√©mentale :</strong> Ajoute seulement les nouveaux objets craftables.
              </div>
            </div>
          </div>

          {/* Import par m√©tier */}
          <div className="job-import-section">
            <h3>‚öíÔ∏è Import par M√©tier</h3>
            <div className="job-import-description">
              <p>Importez les recettes d'un m√©tier sp√©cifique. Utile pour tester ou corriger des donn√©es manquantes.</p>
            </div>

            {availableJobs.length > 0 ? (
              <div className="jobs-grid">
                {availableJobs.map((job) => {
                  // Protection contre les objets invalides
                  if (!job || typeof job !== 'object' || !job.id || !job.name) {
                    console.warn('‚ö†Ô∏è M√©tier invalide ignor√©:', job)
                    return null
                  }

                  // Obtenir l'ic√¥ne et les stats du m√©tier
                  const jobIcon = getJobIcon(job.name)
                  const jobStats = stats?.byProfession?.[job.name] || 0

                  return (
                    <div key={job.id} className="job-item">
                      <div className="job-item-header">
                        <div className="job-info">
                          <div className="job-header">
                            <span className="job-icon">{jobIcon}</span>
                            <div className="job-name">{job.name || `M√©tier ${job.id}`}</div>
                          </div>
                          <div className="job-stats">
                            <span className="job-count">{jobStats} objets</span>
                            <span className="job-id">ID: {job.id}</span>
                          </div>
                        </div>
                        <div className="job-actions">
                        <button
                          onClick={() => handleJobImport(job.id, job.name)}
                          disabled={jobImporting[job.id] || importing || updating}
                          className={`btn btn-sm btn-job-import ${jobStats > 0 ? 'btn-update' : 'btn-new'}`}
                          title={jobStats > 0 ? `Mettre √† jour (${jobStats} objets)` : 'Premier import'}
                        >
                          {jobImporting[job.id] ? 'üîÑ' : jobStats > 0 ? 'üîÑ' : 'üì•'}
                          {jobImporting[job.id] ? 'Import...' : jobStats > 0 ? 'Maj' : 'Import'}
                        </button>

                        <button
                          onClick={() => selectedJobItems[job.name] ?
                            setSelectedJobItems(prev => ({ ...prev, [job.name]: null })) :
                            loadJobItems(job.name)
                          }
                          disabled={loadingItems[job.name]}
                          className="btn btn-sm btn-secondary"
                          title="Voir les objets de ce m√©tier"
                        >
                          {loadingItems[job.name] ? '‚è≥' : selectedJobItems[job.name] ? 'üëÅÔ∏è' : 'üìã'}
                          {selectedJobItems[job.name] ? 'Masquer' : 'Voir items'}
                        </button>

                        {jobStats > 0 && (
                          <button
                            onClick={() => cleanJobDuplicates(job.name)}
                            disabled={importing || updating}
                            className="btn btn-sm btn-warning"
                            title="Nettoyer les doublons"
                          >
                            üßπ Clean
                          </button>
                        )}
                        </div>
                      </div>

                      {/* Affichage des items du m√©tier */}
                      {selectedJobItems[job.name] && (
                        <div className="job-items">
                          <div className="job-items-header">
                            <h4>üìã Items {job.name} ({selectedJobItems[job.name].length})</h4>
                          </div>
                          <div className="job-items-list">
                            {selectedJobItems[job.name].slice(0, 20).map((item, index) => (
                              <div key={item.item_id || index} className="job-item-row">
                                <span className="item-name">{item.item_name}</span>
                                <span className="item-level">Niv. {item.level_required || 1}</span>
                                <span className="item-type">{item.item_type || 'Inconnu'}</span>
                              </div>
                            ))}
                            {selectedJobItems[job.name].length > 20 && (
                              <div className="job-items-more">
                                ... et {selectedJobItems[job.name].length - 20} autres items
                              </div>
                            )}
                          </div>
                        </div>
                      )}
                    </div>
                  )
                })}
              </div>
            ) : (
              <div className="loading">Chargement des m√©tiers...</div>
            )}
          </div>

          {/* Barre de progression */}
          {importing && (
            <div className="import-progress">
              <div className="progress-bar">
                <div 
                  className="progress-fill"
                  style={{ width: `${progress}%` }}
                ></div>
              </div>
              <div className="progress-text">{progress}% termin√©</div>
            </div>
          )}

          {/* Log d'importation */}
          {importLog.length > 0 && (
            <div className="import-log">
              <h3>üìù Journal d'importation</h3>
              <div className="log-container">
                {importLog.map((entry, index) => (
                  <div key={index} className={`log-entry log-${entry.type}`}>
                    <span className="log-time">{entry.timestamp}</span>
                    <span className="log-message">{entry.message}</span>
                  </div>
                ))}
              </div>
            </div>
          )}
        </div>

        <div className="importer-footer">
          <div className="importer-warning">
            ‚ö†Ô∏è L'importation compl√®te peut prendre plusieurs minutes et consommer de la bande passante.
          </div>
        </div>
      </div>
    </div>
  )
}

export default DataImporter
